import pandas as pd
import numpy as np
from statistics import mean

a = [0,1,2,3,4,5,6,7,8]
def calculate(ls9):
  sto = pd.DataFrame(pd.array(ls9).reshape(3,3) )
  permls = []
  retdict = dict()
  for stat in ['mean',  'variance',  'standard deviation',  'max',  'min',  'sum']:
    for ax in range(3):
      tmpls = list()
      tmpls.clear()
      if ax == 0:
        for i in range(3):
          if stat == 'mean':
            tmpls.append(mean(sto.iloc[:,i]))
          elif stat == 'variance':
            tmpls.append(np.var(sto.iloc[:,i]))
          elif stat == 'standard deviation':
            tmpls.append(np.std(sto.iloc[:,i]))
          elif stat == 'max':
            tmpls.append(np.max(sto.iloc[:,i]))
          elif stat == 'min':
            tmpls.append(np.min(sto.iloc[:,i]))
          elif stat == 'sum':
            tmpls.append(np.sum(sto.iloc[:,i]))
      elif ax == 1:
        for i in range(3):
          if stat == 'mean':
            tmpls.append(mean(sto.loc[i]))
          elif stat == 'variance':
            tmpls.append(np.var(sto.loc[i]))
          elif stat == 'standard deviation':
            tmpls.append(np.std(sto.loc[i]))
          elif stat == 'max':
            tmpls.append(np.max(sto.loc[i]))
          elif stat == 'min':
            tmpls.append(np.min(sto.loc[i]))
          elif stat == 'sum':
            tmpls.append(np.sum(sto.loc[i]))
      elif ax == 2:
        for i in range(3):
          if stat == 'mean':
            tmpls = mean(ls9)
          elif stat == 'variance':
            tmpls = np.var(ls9)
          elif stat == 'standard deviation':
            tmpls = np.std(ls9)
          elif stat == 'max':
            tmpls = np.max(ls9)
          elif stat == 'min':
            tmpls = np.min(ls9)
          elif stat == 'sum':
            tmpls = np.sum(ls9)
      permls.append(tmpls)

    retdict.update({stat:permls})
  return retdict

calculate(a)