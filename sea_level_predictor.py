import pandas as pd
import numpy as np
import matplotlib.pyplot as plt
import seaborn as sns
import plotly.express as px
from scipy.stats import linregress

df = pd.read_csv('/content/epa-sea-level.csv')
df.describe()

fig, ax = plt.subplots(figsize=(15,5))
ax.scatter(x=df['Year'], y=df['CSIRO Adjusted Sea Level'])
ax.set_xlabel('Year')
ax.set_ylabel('Sea Level (inches)')
ax.set_title('Rise in Sea Level')

df_clean = df.dropna(subset=['CSIRO Adjusted Sea Level']) #don't drop all na, just target this column
slope, intercept, r_value, p_value, std_err = linregress(df_clean['Year'], df_clean['CSIRO Adjusted Sea Level'])
ax.plot(np.array([1880, 2050]), intercept + slope*np.array([1880, 2050]), color='red', linewidth=5)

slope, intercept, r_value, p_value, std_err = linregress(df_clean.loc[df_clean['Year'] >= 2000,['Year']], df_clean.loc[df_clean['Year'] >= 2000,['CSIRO Adjusted Sea Level']])
ax.plot(np.array([2000, 2050]), intercept + slope*np.array([2000, 2050]), color='green', linewidth=5)

plt.show()