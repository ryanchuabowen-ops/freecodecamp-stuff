def draw_bar_plot():
  # Create 'year' and 'month' columns from the index
  df_bar = df.copy() # Operate on a copy
  df_bar['year'] = df_bar.index.year
  df_bar['month'] = df_bar.index.strftime('%b') # Abbreviated month names

  # Group by year and month and calculate the average page views
  # Then unstack the month to have months as columns for plotting
  df_bar = df_bar.groupby(['year', 'month'])['value'].mean().unstack()

  # Define month order for proper plotting
  month_order = ['Jan', 'Feb', 'Mar', 'Apr', 'May', 'Jun', 'Jul', 'Aug', 'Sep', 'Oct', 'Nov', 'Dec']
  df_bar = df_bar[month_order] # Reorder columns by month

  # Create the bar plot
  fig = df_bar.plot(kind='bar', figsize=(15, 8))
  plt.xlabel('Years')
  plt.ylabel('Average Page Views')
  plt.title('Daily freeCodeCamp Forum Average Page Views per Month (grouped by Year)')
  plt.legend(title='Months', labels=month_order)
  plt.xticks(rotation=90)
  plt.tight_layout()
  plt.show()


draw_bar_plot()

