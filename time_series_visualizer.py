def draw_box_plot():

  df['year'] = df.index.year
  df['month'] = df.index.strftime('%b')

  fig, axes = plt.subplots(1, 2, figsize=(20, 8))
  axes[0].set_title('Year-wise Box Plot (Trend)')
  axes[0].set_xlabel('Year')
  axes[0].set_ylabel('Page Views')
  sns.boxplot(x=df['year'], y=df['value'], ax=axes[0])

  #this reorders the categories of the variable, not the physical rows. When an order of categories is required like in boxplot,
  #the default order would be to arrange them by first appearance in the dataset.
  df['month'] = pd.Categorical(df['month'], categories=['Jan', 'Feb', 'Mar', 'Apr', 'May', 'Jun', 'Jul', 'Aug', 'Sep', 'Oct', 'Nov', 'Dec'])
  axes[1].set_title('Month-wise Box Plot (Seasonality)')
  axes[1].set_xlabel('Month')
  axes[1].set_ylabel('Page Views')
  sns.boxplot(x=df['month'], y=df['value'], ax=axes[1])


  return fig, axes

draw_box_plot()