import pandas as pd
import seaborn as sns
import numpy as np
import matplotlib.pyplot as plt

df = pd.read_csv('/content/medical_examination.csv')
df.head()

df['BMI'] = df['weight'] / (df['height'] / 100) ** 2
df['overweight'] = (df['BMI'] > 25).astype(int)

df['cholesterol'] = df['cholesterol'].replace({1: 0, 2: 1, 3: 1})
df['gluc'] = df['gluc'].replace({1: 0, 2: 1, 3: 1})

def draw_cat_plot():
  df_cat = pd.melt(df, id_vars=['cardio'], value_vars=['active', 'alco', 'cholesterol', 'gluc', 'overweight', 'smoke'])
  df_cat = df_cat.value_counts().reset_index(name='total')
  df_cat['name'] = df_cat['variable'].astype(str) + ' : ' + df_cat['value'].astype(str)

  fig = sns.catplot(x='name', y='total', hue='value', col='cardio', data=df_cat, kind='bar', height=6, aspect=1.2)
  fig.set_axis_labels("Categorical Variable : Value", "Total Count")
  fig.set_xticklabels(rotation=45, ha='right')
  fig.fig.suptitle('Categorical Feature Counts by Cardio Status', y=1.03)
  fig.tight_layout()
  fig.savefig('catplot.png')
  return df_cat, fig


# Call the function to draw the plot
draw_cat_plot()

def draw_heat_map():
  df_heat =   df.loc[(df['ap_lo'] <= df['ap_hi'])].loc[(df['height'] >= df['height'].quantile(0.025))].loc[(df['height'] <= df['height'].quantile(0.975))].loc[(df['weight'] >= df['weight'].quantile(0.025))].loc[(df['weight'] <= df['weight'].quantile(0.975))]
  corr = df_heat.corr()
  mask = np.triu(np.ones_like(corr, dtype=bool)).T
  #masked_corr = np.ma.array(corr, mask=mask)

  fig, ax = plt.subplots(figsize=(12, 12))
  sns.heatmap(corr, mask = mask, annot=True, vmin=-1, vmax=1, cmap = 'coolwarm')
  fig.savefig('heatmap.png')
  return corr, fig


draw_heat_map()

